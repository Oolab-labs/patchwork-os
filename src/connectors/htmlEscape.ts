/**
 * Minimal HTML escape for interpolating untrusted text (vendor error
 * descriptions, exception messages) into OAuth callback HTML pages.
 */
export function escHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
